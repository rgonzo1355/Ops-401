import logging
from logging.handlers import TimedRotatingFileHandler
from colorlog import ColoredFormatter

# Initialize logging
logger = logging.getLogger('RecipeLogger')
logger.setLevel(logging.DEBUG)
handler = TimedRotatingFileHandler('recipes.log', when="midnight", interval=1, backupCount=7)
handler.setFormatter(logging.Formatter('%(asctime)s - %(levelname)s - %(message)s'))
logger.addHandler(handler)
consoleHandler = logging.StreamHandler()
consoleHandler.setFormatter(ColoredFormatter('%(log_color)s%(levelname)-8s%(reset)s %(log_color)s%(message)s'))
logger.addHandler(consoleHandler)

# List of recipes
recipes = ["Spaghetti Carbonara", "Chicken Tikka Masala", "Vegetable Stir Fry", "Beef Stroganoff", "Fish Tacos"]

def display_recipes():
    logger.info("Displaying all recipes")
    for index, recipe in enumerate(recipes, start=1):
        print(f"{index}. {recipe}")

def add_recipe():
    new_recipe = input("Enter the name of the new recipe: ")
    recipes.append(new_recipe)
    logger.info(f"Added new recipe: {new_recipe}")

def update_recipe():
    display_recipes()
    recipe_number = int(input("Enter the number of the recipe you want to update: "))
    if 1 <= recipe_number <= len(recipes):
        new_recipe_name = input("Enter the new name of the recipe: ")
        recipes[recipe_number - 1] = new_recipe_name
        logger.info(f"Updated recipe #{recipe_number} to {new_recipe_name}")
    else:
        logger.error("Invalid recipe number")

def clear_recipes():
    recipes.clear()
    logger.info("Cleared all recipes")

def main_menu():
    while True:
        print("\nRecipe Manager")
        print("1. Display Recipes")
        print("2. Add a Recipe")
        print("3. Update a Recipe")
        print("4. Clear All Recipes")
        print("5. Exit")
        choice = input("Enter your choice: ")
        
        if choice == "1":
            display_recipes()
        elif choice == "2":
            add_recipe()
        elif choice == "3":
            update_recipe()
        elif choice == "4":
            clear_recipes()
        elif choice == "5":
            print("Exiting Recipe Manager. Goodbye!")
            break
        else:
            print("Invalid choice. Please enter a number between 1 and 5.")

if __name__ == "__main__":
    main_menu()

























































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































